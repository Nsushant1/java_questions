//Write a program that declares an array of 5 integers and prints their values.

package assign;

public class Array {
    public static void main(String[] args) {
        int[] array = { 10, 20, 30, 40, 50 };

        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i]);
        }

    }

}
